//Monster
//
//A class to define the monster's behavior.
//The monster will be controlled by the input keys to move it up, down, left and right.

//Monster constructor
//
//Sets the properties
function Monster(x,y,vx,vy,size,speed){
  this.x = x;
  this.y = y;
  this.vx = vx;
  this.vy = vy;
  this.size = size;
  this.speed = speed;
}
